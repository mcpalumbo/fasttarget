def read_curation_tsv(curation_tsv):
    """
    Read user-curated Boltz parameters from TSV.

    :param curation_tsv: Path to boltz_curation.tsv.
    :return: Dictionary keyed by locus_tag with run flag, copies, and ligands.
    """
    if not os.path.isfile(curation_tsv):
        raise FileNotFoundError(f'Curation TSV not found: {curation_tsv}')
    df = pd.read_csv(curation_tsv, sep='\t', dtype=str, keep_default_na=False)
    curation = {}
    for _, row in df.iterrows():
        locus_tag = row.get('locus_tag', '').strip()
        if not locus_tag:
            continue
        run_value = row.get('run_boltz', 'True').strip().lower()
        try:
            copies = int(row.get('copies', '1') or 1)
        except ValueError:
            copies = 1
        curation[locus_tag] = {
            'run_boltz': run_value in ('true', '1', 'yes', 'y'),
            'copies': max(copies, 1),
            'quaternary_structure': row.get('quaternary_structure', UNKNOWN) or UNKNOWN,
            'cofactors_uniprot': (
                row.get('cofactors_uniprot_raw')
                or row.get('cofactors_uniprot')
                or UNKNOWN
            ),
            'substrates_uniprot': row.get('substrates_uniprot_raw', UNKNOWN) or UNKNOWN,
            'cofactor_ccds': split_clean(row.get('cofactor_ccds', '')),
            'cofactor_smiles': split_clean(row.get('cofactor_smiles', '')),
            'selected_ligand_names': split_clean(row.get('selected_ligand_names', '')),
            'selected_ligand_smiles': split_clean(
                row.get('selected_ligand_smiles')
                or row.get('cofactor_smiles', '')
            ),
        }
    return curation


def chain_ids(copies):
    """
    Create Boltz protein chain IDs for the requested copy number.

    :param copies: Number of protein copies in the model.
    :return: List of chain IDs.
    """
    alphabet = list('ABCDEFGHIJKLMNOPQRSTUVWXYZ')
    if copies <= len(alphabet):
        return alphabet[:copies]
    return [f'A{i + 1}' for i in range(copies)]


def ligand_ids(prefix, copies):
    """
    Create ligand IDs, assuming one ligand per protein copy.

    :param prefix: Ligand ID prefix.
    :param copies: Number of protein copies.
    :return: List of ligand IDs.
    """
    prefix = re.sub(r'\([^)]*\)', '', str(prefix))
    safe_prefix = re.sub(r'[^A-Za-z0-9]', '', prefix.upper()) or 'LIG'
    return [f'{safe_prefix}{i + 1}' for i in range(copies)]


def ligand_id_prefix(name, index, total):
    """
    Create a compact ligand ID prefix from a ligand name.

    :param name: Ligand name.
    :param index: One-based ligand index.
    :param total: Total number of ligands in the YAML.
    :return: Ligand ID prefix.
    """
    if name:
        cleaned = re.sub(r'\([^)]*\)', '', str(name))
        cleaned = re.sub(r'[^A-Za-z0-9]', '', cleaned.upper())
        if 1 <= len(cleaned) <= 6:
            return cleaned
    return 'LIG' if total == 1 else f'L{index}'


def build_boltz_yaml_dict(locus_tag, sequence, curation):
    """
    Build the Boltz YAML data structure for one locus tag.

    :param locus_tag: Locus tag identifier.
    :param sequence: Protein sequence.
    :param curation: Curated Boltz parameters for the locus.
    :return: Dictionary ready to write as YAML.
    """
    copies = curation['copies']
    yaml_dict = {
        'version': 1,
        'sequences': [
            {
                'protein': {
                    'id': chain_ids(copies),
                    'sequence': sequence,
                }
            }
        ]
    }

    smiles_values = curation.get('selected_ligand_smiles') or curation.get('cofactor_smiles', [])
    ligand_names = curation.get('selected_ligand_names', [])
    for idx, smiles in enumerate(smiles_values, start=1):
        name = ligand_names[idx - 1] if idx <= len(ligand_names) else None
        prefix = ligand_id_prefix(name, idx, len(smiles_values))
        yaml_dict['sequences'].append({
            'ligand': {
                'id': ligand_ids(prefix, copies),
                'smiles': smiles,
            }
        })

    return yaml_dict


def write_boltz_yaml_files(annotations, curation, inputs_dir):
    """
    Write one YAML input per locus selected in the curation TSV.

    :param annotations: Dictionary keyed by locus_tag with Boltz annotations.
    :param curation: Dictionary keyed by locus_tag with curated Boltz parameters.
    :param inputs_dir: Directory where YAML files will be written.
    :return: Dictionary keyed by locus_tag with YAML paths.
    """
    yaml_files = {}
    for locus_tag in sorted(annotations):
        ann = annotations[locus_tag]
        cur = curation.get(locus_tag)
        sequence = ann.get('sequence')
        if not cur or not cur['run_boltz'] or not sequence:
            continue

        yaml_path = os.path.join(inputs_dir, f'{locus_tag}.yaml')
        yaml_data = build_boltz_yaml_dict(locus_tag, sequence, cur)
        with open(yaml_path, 'w') as handle:
            handle.write(f'# {locus_tag}\n')
            yaml.safe_dump(yaml_data, handle, sort_keys=False, width=120)
        yaml_files[locus_tag] = yaml_path

    print(f'Boltz YAML files written: {len(yaml_files)}')
    return yaml_files


def run_boltz_predict(inputs_dir, results_dir, cache, use_msa_server=True, output_format='pdb', boltz_executable='boltz'):
    """
    Run Boltz on a folder of YAML files.

    :param inputs_dir: Directory containing Boltz YAML files.
    :param results_dir: Directory where Boltz will write outputs.
    :param cache: Boltz cache path.
    :param use_msa_server: If True, pass --use_msa_server.
    :param output_format: Boltz output structure format.
    :param boltz_executable: Boltz executable name or path.
    """
    cmd = [
        boltz_executable,
        'predict',
        inputs_dir,
    ]
    if use_msa_server:
        cmd.append('--use_msa_server')
    cmd.extend([
        '--out_dir',
        results_dir,
        '--cache',
        cache,
        '--output_format',
        output_format,
    ])

    print('Running:', ' '.join(cmd))
    subprocess.run(cmd, check=True)


def model_sort_key(path):
    """
    Sort Boltz model candidates so preferred model names are selected first.

    :param path: Candidate model path.
    :return: Sort key tuple.
    """
    name = os.path.basename(path).lower()
    score = 0
    if 'model_0' in name or 'rank_001' in name or 'rank_1' in name:
        score -= 100
    if name.endswith('.pdb'):
        score -= 10
    return score, name


def find_best_boltz_model(results_dir, locus_tag):
    """
    Find the preferred Boltz model for a locus tag.

    :param results_dir: Boltz results directory.
    :param locus_tag: Locus tag identifier.
    :return: Path to the selected model file, or None.
    """
    search_roots = [
        os.path.join(results_dir, 'predictions', locus_tag),
        os.path.join(results_dir, locus_tag),
        results_dir,
    ]
    candidates = []
    for root in search_roots:
        if os.path.isdir(root):
            candidates.extend(glob.glob(os.path.join(root, '**', '*.pdb'), recursive=True))
            candidates.extend(glob.glob(os.path.join(root, '**', '*.cif'), recursive=True))

    candidates = [
        path for path in candidates
        if os.path.isfile(path) and locus_tag.lower() in os.path.basename(path).lower()
    ] or [path for path in candidates if os.path.isfile(path)]

    if not candidates:
        return None
    return sorted(set(candidates), key=model_sort_key)[0]


def normalize_boltz_models(yaml_files, results_dir, models_dir):
    """
    Copy best Boltz models into a single folder named BZ_<locus_tag>.<ext>.

    :param yaml_files: Dictionary keyed by locus_tag with YAML paths.
    :param results_dir: Boltz results directory.
    :param models_dir: Directory where normalized models will be copied.
    :return: Dictionary keyed by locus_tag with normalized model paths.
    """
    model_paths = {}
    for locus_tag in tqdm(sorted(yaml_files), desc='Normalizing Boltz models'):
        source = find_best_boltz_model(results_dir, locus_tag)
        if not source:
            logging.warning(f'No Boltz model found for {locus_tag}')
            continue
        ext = os.path.splitext(source)[1].lower()
        dest = os.path.join(models_dir, f'BZ_{locus_tag}{ext}')
        shutil.copy2(source, dest)
        model_paths[locus_tag] = dest
    print(f'Normalized Boltz models: {len(model_paths)}')
    return model_paths


