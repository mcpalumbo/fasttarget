def create_boltz_folders(structures_dir):
    """
    Create the standalone Boltz directory tree inside a FastTarget structures folder.

    :param structures_dir: Path to organism/<organism>/structures.
    :return: Dictionary with key output directories.
    """
    boltz_dir = os.path.join(structures_dir, 'boltz')
    dirs = {
        'boltz': boltz_dir,
        'inputs': os.path.join(boltz_dir, 'inputs'),
        'results': os.path.join(boltz_dir, 'results'),
        'models': os.path.join(boltz_dir, 'models'),
        'pockets': os.path.join(boltz_dir, 'pockets'),
    }
    for directory in dirs.values():
        os.makedirs(directory, exist_ok=True)
    return dirs


def infer_organism_name(output_path=None, organism_name=None, structures_dir=None):
    """
    Resolve organism name and structures directory from user inputs.

    :param output_path: FastTarget output directory containing organism folders.
    :param organism_name: Organism name used in the FastTarget run.
    :param structures_dir: Direct path to organism/<organism>/structures.
    :return: Tuple with organism name and absolute structures directory.
    """
    if structures_dir:
        structures_dir = os.path.abspath(structures_dir)
        organism_dir = os.path.dirname(structures_dir)
        return os.path.basename(organism_dir), structures_dir

    if not output_path or not organism_name:
        raise ValueError('Provide either --structures-dir or both --output-path and --organism-name.')

    structures_dir = os.path.join(os.path.abspath(output_path), organism_name, 'structures')
    return organism_name, structures_dir


def find_gbk_file(output_path=None, organism_name=None, gbk_file=None, structures_dir=None):
    """
    Find the GenBank file used by FastTarget.

    :param output_path: FastTarget output directory containing organism folders.
    :param organism_name: Organism name used in the FastTarget run.
    :param gbk_file: Explicit GenBank file path.
    :param structures_dir: Direct path to organism/<organism>/structures.
    :return: Path to the GenBank file.
    """
    if gbk_file:
        gbk_file = os.path.abspath(gbk_file)
        if not os.path.isfile(gbk_file):
            raise FileNotFoundError(f'GBK file not found: {gbk_file}')
        return gbk_file

    candidates = []
    if output_path and organism_name:
        organism_dir = os.path.join(os.path.abspath(output_path), organism_name)
        candidates.extend([
            os.path.join(organism_dir, 'genome', f'{organism_name}.gbk'),
            os.path.join(organism_dir, f'{organism_name}.gbk'),
        ])
    if structures_dir:
        organism_dir = os.path.dirname(os.path.abspath(structures_dir))
        name = os.path.basename(organism_dir)
        candidates.extend([
            os.path.join(organism_dir, 'genome', f'{name}.gbk'),
            os.path.join(organism_dir, f'{name}.gbk'),
        ])

    for candidate in candidates:
        if os.path.isfile(candidate):
            return candidate

    raise FileNotFoundError('Could not find GBK file. Use --gbk-file.')


def load_locus_uniprot_mapping(structures_dir, organism_name):
    """
    Load locus_tag -> UniProt accession mapping generated by FastTarget.

    :param structures_dir: Path to organism/<organism>/structures.
    :param organism_name: Organism name used in the FastTarget run.
    :return: Dictionary keyed by locus_tag with UniProt accession values.
    """
    mapping_file = os.path.join(
        structures_dir,
        'uniprot_files',
        f'uniprot_{organism_name}_id_mapping.json'
    )
    if not files.file_check(mapping_file):
        raise FileNotFoundError(f'UniProt mapping file not found: {mapping_file}')

    mapping = files.json_to_dict(mapping_file)
    normalized = {}
    for locus_tag, uniprot_id in mapping.items():
        if isinstance(uniprot_id, list):
            uniprot_id = uniprot_id[0] if uniprot_id else None
        if uniprot_id:
            normalized[locus_tag] = str(uniprot_id)
    return normalized


def get_locus_sequences(gbk_file):
    """
    Extract translated CDS sequences keyed by locus_tag from a GenBank file.

    :param gbk_file: Path to the organism GenBank file.
    :return: Dictionary keyed by locus_tag with translated protein sequences.
    """
    sequences = {}
    for record in SeqIO.parse(gbk_file, 'genbank'):
        for feature in record.features:
            if feature.type != 'CDS':
                continue
            locus_tags = feature.qualifiers.get('locus_tag', [])
            translations = feature.qualifiers.get('translation', [])
            if not locus_tags or not translations:
                continue
            sequences[locus_tags[0]] = translations[0].replace('*', '')
    return sequences


def split_clean(value):
    """
    Split user-editable TSV fields that may contain pipe, comma, or semicolon lists.

    :param value: Raw TSV field value.
    :return: List of non-empty cleaned values.
    """
    if value is None:
        return []
    text = str(value).strip()
    if not text or text.lower() in ('nan', 'none', 'unknown'):
        return []
    return [item.strip() for item in re.split(r'[|;,]', text) if item.strip()]


def text_from_element(element):
    """
    Return all text from an XML element as a compact single line.

    :param element: XML element to parse.
    :return: String with normalized whitespace, or None.
    """
    if element is None:
        return None
    text = ' '.join(part.strip() for part in element.itertext() if part and part.strip())
    return re.sub(r'\s+', ' ', text).strip() or None


def first_xml_text_by_local_name(root, local_name):
    """
    Return the first text value from an XML tree matching a local tag name.

    :param root: XML root element.
    :param local_name: Tag local name without namespace.
    :return: Text value, or None.
    """
    for element in root.iter():
        if element.tag.split('}')[-1] == local_name and element.text:
            return element.text.strip()
    return None


def normalize_ligand_name(name):
    """
    Normalize ligand names for dictionary lookup and duplicate removal.

    :param name: Raw ligand name.
    :return: Normalized ligand name.
    """
    if not name:
        return ''
    name = re.sub(r'\s+', ' ', str(name)).strip()
    return name.rstrip('.')


def normalize_match_text(text):
    """
    Normalize chemical names for conservative string matching.

    :param text: Raw chemical name.
    :return: Normalized text for matching.
    """
    text = normalize_ligand_name(text).lower()
    text = text.replace('^{+}', '+').replace('^+', '+')
    text = text.replace('_{2}', '2').replace('_{3}', '3').replace('_{4}', '4')
    text = re.sub(r'[\s_{}]', '', text)
    return text


def unique_ligands(ligands):
    """
    Deduplicate ligand records by name and ChEBI ID.

    :param ligands: List of ligand dictionaries.
    :return: Deduplicated ligand dictionary list.
    """
    unique = []
    seen = set()
    for ligand in ligands:
        name = normalize_ligand_name(ligand.get('name'))
        chebi = ligand.get('chebi') or ''
        key = (name.lower(), chebi)
        if not name or key in seen:
            continue
        seen.add(key)
        clean_ligand = dict(ligand)
        clean_ligand['name'] = name
        unique.append(clean_ligand)
    return unique


def parse_reaction_substrates(reaction_text):
    """
    Parse only the substrate side of a UniProt catalytic reaction string.

    :param reaction_text: Raw reaction text from UniProt.
    :return: List of substrate names from the left side of the reaction.
    """
    if not reaction_text:
        return []
    text = re.sub(r'\s+', ' ', reaction_text).strip().rstrip('.')
    text = re.split(r'\s(?:=|<=>|->|=>)\s', text, maxsplit=1)[0]
    substrates = []
    for part in re.split(r'\s+\+\s+', text):
        part = re.sub(r'^\d+(\.\d+)?\s+', '', part.strip())
        part = re.sub(r'\s*\[[^\]]+\]\s*$', '', part)
        part = normalize_ligand_name(part)
        if part:
            substrates.append(part)
    return substrates


def chebi_soap_request(action, body):
    """
    Send a minimal ChEBI SOAP request and return the XML root.

    :param action: ChEBI SOAP action name.
    :param body: SOAP body XML content.
    :return: Parsed XML root element.
    """
    envelope = f"""<?xml version="1.0" encoding="UTF-8"?>
<soapenv:Envelope xmlns:soapenv="http://schemas.xmlsoap.org/soap/envelope/"
                  xmlns:chebi="https://www.ebi.ac.uk/webservices/chebi">
  <soapenv:Header/>
  <soapenv:Body>
    {body}
  </soapenv:Body>
</soapenv:Envelope>"""
    headers = {
        'Content-Type': 'text/xml;charset=UTF-8',
        'SOAPAction': action,
        'User-Agent': 'FastTarget-Boltz/1.0',
    }
    response = requests.post(CHEBI_SOAP_URL, data=envelope.encode('utf-8'), headers=headers, timeout=40)
    response.raise_for_status()
    return ET.fromstring(response.content)


def fetch_chebi_complete_entity(chebi_id):
    """
    Fetch a complete ChEBI entity using REST first and SOAP as fallback.

    :param chebi_id: ChEBI identifier, e.g. CHEBI:12345.
    :return: Dictionary with ChEBI ID, name, and SMILES when available.
    """
    if not chebi_id:
        return {}
    rest_entity = fetch_chebi_complete_entity_rest(chebi_id)
    if rest_entity:
        return rest_entity

    chebi_id = xml_escape(str(chebi_id))
    body = f"""
<chebi:getCompleteEntity>
  <chebi:chebiId>{chebi_id}</chebi:chebiId>
</chebi:getCompleteEntity>"""
    try:
        root = chebi_soap_request('getCompleteEntity', body)
    except Exception as exc:
        logging.warning(f'ChEBI getCompleteEntity failed for {chebi_id}: {exc}')
        return {}

    synonyms = []
    for element in root.iter():
        local_name = element.tag.split('}')[-1]
        if local_name in ('chebiAsciiName', 'data', 'synonym') and element.text:
            synonyms.append(normalize_ligand_name(element.text))

    return {
        'chebi': first_xml_text_by_local_name(root, 'chebiId') or chebi_id,
        'name': first_xml_text_by_local_name(root, 'chebiAsciiName'),
        'smiles': first_xml_text_by_local_name(root, 'smiles'),
        'synonyms': sorted({synonym for synonym in synonyms if synonym}),
    }


def fetch_chebi_complete_entity_rest(chebi_id):
    """
    Fetch a complete ChEBI entity using the ChEBI JSON REST API.

    :param chebi_id: ChEBI identifier, e.g. CHEBI:12345.
    :return: Dictionary with ChEBI ID, name, SMILES, and synonyms when available.
    """
    if not chebi_id:
        return {}

    numeric_id = str(chebi_id).replace('CHEBI:', '').strip()
    if not numeric_id:
        return {}

    url = f'{CHEBI_REST_URL}/{numeric_id}/'
    params = {
        'only_ontology_parents': 'false',
        'only_ontology_children': 'false',
    }
    try:
        response = requests.get(url, params=params, timeout=40, headers={'User-Agent': 'FastTarget-Boltz/1.0'})
        if response.status_code == 404:
            return {}
        response.raise_for_status()
        data = response.json()
    except Exception as exc:
        logging.warning(f'ChEBI REST lookup failed for {chebi_id}: {exc}')
        return {}

    synonyms = []
    names = data.get('names') or {}
    for name_records in names.values():
        if not isinstance(name_records, list):
            continue
        for record in name_records:
            if not isinstance(record, dict):
                continue
            for key in ('ascii_name', 'name'):
                if record.get(key):
                    synonyms.append(normalize_ligand_name(record.get(key)))

    smiles = None
    default_structure = data.get('default_structure') or {}
    if isinstance(default_structure, dict):
        smiles = default_structure.get('smiles')

    return {
        'chebi': data.get('chebi_accession') or f'CHEBI:{numeric_id}',
        'name': data.get('ascii_name') or data.get('name'),
        'smiles': smiles,
        'synonyms': sorted({synonym for synonym in synonyms if synonym}),
    }


def fetch_rhea_reaction_participants(rhea_id):
    """
    Fetch Rhea reaction participant names and ChEBI IDs from the Rhea REST table.

    :param rhea_id: Rhea identifier, e.g. RHEA:16373.
    :return: Dictionary with equation, ChEBI names, and ChEBI IDs.
    """
    if not rhea_id:
        return {}
    params = {
        'query': rhea_id,
        'columns': 'rhea-id,equation,chebi,chebi-id',
        'format': 'tsv',
        'limit': 1,
    }
    try:
        response = requests.get(RHEA_TABLE_URL, params=params, timeout=40, headers={'User-Agent': 'FastTarget-Boltz/1.0'})
        response.raise_for_status()
        lines = [line for line in response.text.splitlines() if line.strip()]
        if len(lines) < 2:
            return {}
        header = lines[0].split('\t')
        values = lines[1].split('\t')
        row = dict(zip(header, values))
        return {
            'equation': row.get('Equation') or row.get('equation'),
            'chebi_names': split_clean(row.get('ChEBI name') or row.get('chebi') or ''),
            'chebi_ids': split_clean(row.get('ChEBI identifier') or row.get('chebi-id') or ''),
        }
    except Exception as exc:
        logging.warning(f'Rhea lookup failed for {rhea_id}: {exc}')
        return {}


def fetch_uniprot_batch_xml(uniprot_ids):
    """
    Fetch UniProt XML data for multiple UniProt IDs without importing structures.py.

    :param uniprot_ids: List of UniProt accession IDs.
    :return: XML content as bytes.
    """
    if not uniprot_ids:
        return b'<?xml version="1.0" encoding="UTF-8"?><uniprot xmlns="http://uniprot.org/uniprot"></uniprot>'

    ids_string = ','.join(uniprot_ids)
    uniprot_url = f'https://rest.uniprot.org/uniprotkb/accessions?accessions={ids_string}&format=xml'
    headers = {
        'User-Agent': 'FastTarget-Boltz/1.0',
        'Accept': 'application/xml',
    }
    response = requests.get(uniprot_url, headers=headers, timeout=60)
    response.raise_for_status()
    return response.content


def search_chebi_id_by_name(name):
    """
    Search ChEBI for a ligand name and return the first ChEBI ID.

    :param name: Ligand name.
    :return: ChEBI ID, or None.
    """
    if not name:
        return None
    search = xml_escape(str(name))
    body = f"""
<chebi:getLiteEntity>
  <chebi:search>{search}</chebi:search>
  <chebi:searchCategory>ALL</chebi:searchCategory>
  <chebi:maximumResults>1</chebi:maximumResults>
  <chebi:stars>ALL</chebi:stars>
</chebi:getLiteEntity>"""
    try:
        root = chebi_soap_request('getLiteEntity', body)
        return first_xml_text_by_local_name(root, 'chebiId')
    except Exception as exc:
        logging.warning(f'ChEBI name search failed for {name}: {exc}')
        return None


def fetch_pubchem_smiles(name):
    """
    Fetch canonical SMILES from PubChem by compound name as a fallback.

    :param name: Ligand name.
    :return: SMILES string, or None.
    """
    if not name:
        return None
    url = (
        'https://pubchem.ncbi.nlm.nih.gov/rest/pug/compound/name/'
        f'{quote(name)}/property/CanonicalSMILES/TXT'
    )
    try:
        response = requests.get(url, timeout=30, headers={'User-Agent': 'FastTarget-Boltz/1.0'})
        if response.status_code == 404:
            return None
        response.raise_for_status()
        smiles = response.text.strip().splitlines()[0]
        return smiles or None
    except Exception as exc:
        logging.warning(f'PubChem SMILES lookup failed for {name}: {exc}')
        return None


def resolve_ligand_smiles(ligand, ligand_cache):
    """
    Resolve a UniProt ligand record to SMILES.

    :param ligand: Ligand dictionary with name and optional ChEBI ID.
    :param ligand_cache: Mutable cache keyed by ligand name/ChEBI ID.
    :return: Ligand dictionary with resolved SMILES metadata.
    """
    name = normalize_ligand_name(ligand.get('name'))
    chebi = ligand.get('chebi')
    cache_key = f'{chebi or ""}|{name.lower()}'
    if cache_key in ligand_cache:
        return dict(ligand_cache[cache_key])

    resolved = {
        'name': name,
        'chebi': chebi,
        'smiles': None,
        'source': None,
        'needs_manual_smiles': True,
    }

    if not chebi:
        chebi = search_chebi_id_by_name(name)
        resolved['chebi'] = chebi

    if chebi:
        entity = fetch_chebi_complete_entity(chebi)
        if entity.get('smiles'):
            resolved.update({
                'name': entity.get('name') or name,
                'chebi': entity.get('chebi') or chebi,
                'smiles': entity.get('smiles'),
                'source': 'chebi',
                'needs_manual_smiles': False,
            })
            ligand_cache[cache_key] = resolved
            return dict(resolved)

    if name in LOCAL_LIGAND_SMILES:
        resolved.update({
            'smiles': LOCAL_LIGAND_SMILES[name],
            'source': 'local',
            'needs_manual_smiles': False,
        })
        ligand_cache[cache_key] = resolved
        return dict(resolved)

    pubchem_smiles = fetch_pubchem_smiles(name)
    if pubchem_smiles:
        resolved.update({
            'smiles': pubchem_smiles,
            'source': 'pubchem',
            'needs_manual_smiles': False,
        })

    ligand_cache[cache_key] = resolved
    return dict(resolved)


def chebi_entity_matches_name(entity, name):
    """
    Check whether a ChEBI entity matches a substrate name.

    :param entity: Dictionary returned by fetch_chebi_complete_entity.
    :param name: Substrate name from the reaction left side.
    :return: True if the entity name or synonym matches the substrate name.
    """
    target = normalize_match_text(name)
    if not target:
        return False
    candidates = [entity.get('name')] + entity.get('synonyms', [])
    for candidate in candidates:
        candidate_norm = normalize_match_text(candidate)
        if candidate_norm and (candidate_norm == target or candidate_norm in target or target in candidate_norm):
            return True
    return False


def reaction_chebi_ids_from_rhea(rhea_ids):
    """
    Retrieve reaction participant ChEBI IDs from Rhea identifiers.

    :param rhea_ids: List of Rhea identifiers.
    :return: List of ChEBI identifiers.
    """
    chebi_ids = []
    for rhea_id in rhea_ids:
        rhea_data = fetch_rhea_reaction_participants(rhea_id)
        chebi_ids.extend(rhea_data.get('chebi_ids', []))
    return sorted(set(chebi_ids))


def match_substrate_ligands(substrate_names, reaction_chebi_ids, rhea_ids=None):
    """
    Match reaction-side substrate names to reaction participant ChEBI IDs.

    :param substrate_names: Substrate names parsed from the reaction left side.
    :param reaction_chebi_ids: ChEBI IDs listed in the UniProt reaction reference.
    :param rhea_ids: Optional Rhea identifiers used as fallback for participant ChEBIs.
    :return: List of ligand dictionaries with substrate name and matched ChEBI ID.
    """
    if not substrate_names:
        return []

    candidate_chebi_ids = list(reaction_chebi_ids or [])
    if not candidate_chebi_ids and rhea_ids:
        candidate_chebi_ids = reaction_chebi_ids_from_rhea(rhea_ids)

    candidate_entities = []
    for chebi_id in sorted(set(candidate_chebi_ids)):
        entity = fetch_chebi_complete_entity(chebi_id)
        if entity:
            candidate_entities.append(entity)

    matched = []
    for substrate_name in substrate_names:
        match = None
        for entity in candidate_entities:
            if chebi_entity_matches_name(entity, substrate_name):
                match = entity
                break
        if match:
            matched.append({
                'name': substrate_name,
                'chebi': match.get('chebi'),
            })
        else:
            matched.append({
                'name': substrate_name,
                'chebi': None,
            })
    return unique_ligands(matched)


def resolve_ligand_list(ligands, ligand_cache):
    """
    Resolve a list of ligand records to SMILES.

    :param ligands: List of ligand dictionaries.
    :param ligand_cache: Mutable ligand resolution cache.
    :return: List of resolved ligand dictionaries.
    """
    return [resolve_ligand_smiles(ligand, ligand_cache) for ligand in unique_ligands(ligands)]


def pipe_join(values):
    """
    Join non-empty values as a pipe-separated TSV field.

    :param values: Iterable of values.
    :return: Pipe-separated string or Unknown.
    """
    cleaned = [str(value) for value in values if value not in (None, '', UNKNOWN)]
    return '|'.join(cleaned) if cleaned else UNKNOWN


def parse_uniprot_boltz_entry(entry):
    """
    Parse the UniProt XML fields needed to create Boltz curation files.

    :param entry: UniProt XML entry element.
    :return: Dictionary with Boltz-relevant UniProt annotations, or None.
    """
    accession_el = entry.find('up:accession', UNIPROT_NS)
    if accession_el is None or not accession_el.text:
        return None

    accession = accession_el.text
    protein_name = UNKNOWN
    recommended = entry.find('up:protein/up:recommendedName/up:fullName', UNIPROT_NS)
    submitted = entry.find('up:protein/up:submittedName/up:fullName', UNIPROT_NS)
    if recommended is not None and recommended.text:
        protein_name = recommended.text
    elif submitted is not None and submitted.text:
        protein_name = submitted.text

    cofactors = []
    for comment in entry.findall('up:comment[@type="cofactor"]', UNIPROT_NS):
        comment_cofactors = []
        for cofactor_el in comment.findall('up:cofactor', UNIPROT_NS):
            name_el = cofactor_el.find('up:name', UNIPROT_NS)
            cofactor = {
                'name': name_el.text if name_el is not None and name_el.text else UNKNOWN,
                'chebi': None,
            }
            db_ref = cofactor_el.find('up:dbReference[@type="ChEBI"]', UNIPROT_NS)
            if db_ref is not None:
                cofactor['chebi'] = db_ref.get('id')
            comment_cofactors.append(cofactor)

        if comment_cofactors:
            cofactors.extend(comment_cofactors)
        else:
            text = text_from_element(comment)
            if text:
                cofactors.append({'name': text, 'chebi': None})

    catalytic_substrates = []
    for comment in entry.findall('up:comment[@type="catalytic activity"]', UNIPROT_NS):
        reaction = comment.find('up:reaction', UNIPROT_NS)
        reaction_text = text_from_element(reaction)
        if reaction is None:
            continue
        reaction_chebi_ids = [
            db_ref.get('id')
            for db_ref in reaction.findall('up:dbReference[@type="ChEBI"]', UNIPROT_NS)
            if db_ref.get('id')
        ]
        rhea_ids = [
            db_ref.get('id')
            for db_ref in reaction.findall('up:dbReference[@type="Rhea"]', UNIPROT_NS)
            if db_ref.get('id')
        ]
        substrate_names = parse_reaction_substrates(reaction_text)
        catalytic_substrates.extend(match_substrate_ligands(substrate_names, reaction_chebi_ids, rhea_ids))

    subunit_comment = entry.find('up:comment[@type="subunit"]', UNIPROT_NS)
    subunit_text = text_from_element(subunit_comment) or UNKNOWN
    quaternary_structure, copies = infer_quaternary_structure(subunit_text)

    sequence_el = entry.find('up:sequence', UNIPROT_NS)
    uniprot_sequence = None
    if sequence_el is not None and sequence_el.text:
        uniprot_sequence = re.sub(r'\s+', '', sequence_el.text)

    return {
        'uniprot_id': accession,
        'protein_name': protein_name,
        'cofactors': cofactors if cofactors else [],
        'catalytic_substrates': unique_ligands(catalytic_substrates),
        'subunit_text': subunit_text,
        'quaternary_structure': quaternary_structure,
        'copies': copies,
        'uniprot_sequence': uniprot_sequence,
    }


def infer_quaternary_structure(subunit_text):
    """
    Infer copy number from UniProt subunit text. Defaults to one copy for YAML creation.

    :param subunit_text: UniProt subunit comment text.
    :return: Tuple with inferred quaternary structure label and copy number.
    """
    if not subunit_text or subunit_text == UNKNOWN:
        return UNKNOWN, 1

    text = subunit_text.lower()
    patterns = [
        ('homodimer', 2),
        ('homotrimer', 3),
        ('homotetramer', 4),
        ('homopentamer', 5),
        ('homohexamer', 6),
        ('homoheptamer', 7),
        ('homooctamer', 8),
        ('homononamer', 9),
        ('homodecamer', 10),
        ('monomer', 1),
    ]
    for label, copies in patterns:
        if label in text:
            return label, copies

    if 'dimer' in text and 'heterodimer' not in text:
        return 'dimer', 2
    if 'trimer' in text and 'heterotrimer' not in text:
        return 'trimer', 3
    if 'tetramer' in text and 'heterotetramer' not in text:
        return 'tetramer', 4
    if 'hexamer' in text and 'heterohexamer' not in text:
        return 'hexamer', 6

    return UNKNOWN, 1


def fetch_uniprot_boltz_annotations(uniprot_ids, batch_size=100):
    """
    Fetch UniProt annotations in batches using the existing FastTarget UniProt helper.

    :param uniprot_ids: Iterable of UniProt accession IDs.
    :param batch_size: Number of UniProt IDs to request per batch.
    :return: Dictionary keyed by UniProt accession with parsed annotations.
    """
    annotations = {}
    unique_ids = sorted({uid for uid in uniprot_ids if uid})
    for index in tqdm(range(0, len(unique_ids), batch_size), desc='UniProt batches'):
        batch = unique_ids[index:index + batch_size]
        try:
            xml_content = fetch_uniprot_batch_xml(batch)
            root = ET.fromstring(xml_content)
            for entry in root.findall('up:entry', UNIPROT_NS):
                parsed = parse_uniprot_boltz_entry(entry)
                if parsed:
                    annotations[parsed['uniprot_id']] = parsed
        except Exception as exc:
            logging.exception(f'Failed to fetch UniProt batch {batch}: {exc}')
            for uniprot_id in batch:
                annotations.setdefault(uniprot_id, {
                    'uniprot_id': uniprot_id,
                    'protein_name': UNKNOWN,
                    'cofactors': [],
                    'catalytic_substrates': [],
                    'subunit_text': UNKNOWN,
                    'quaternary_structure': UNKNOWN,
                    'copies': 1,
                    'uniprot_sequence': None,
                })
    return annotations


def build_boltz_annotations(mapping, sequences, refresh_uniprot=False, annotations_json=None):
    """
    Build locus-keyed Boltz annotations from mapping, GBK sequences, and UniProt.

    :param mapping: Dictionary keyed by locus_tag with UniProt accession values.
    :param sequences: Dictionary keyed by locus_tag with protein sequences.
    :param refresh_uniprot: If True, ignore existing annotation JSON and fetch again.
    :param annotations_json: Existing Boltz annotations JSON path, if available.
    :return: Dictionary keyed by locus_tag with Boltz annotations.
    """
    existing = {}
    if annotations_json and os.path.isfile(annotations_json) and not refresh_uniprot:
        existing = files.json_to_dict(annotations_json)

    missing_loci = [locus for locus in mapping if locus not in existing]
    uniprot_annotations = fetch_uniprot_boltz_annotations([mapping[locus] for locus in missing_loci])
    ligand_cache = {}

    annotations = dict(existing)
    for locus_tag, uniprot_id in mapping.items():
        if locus_tag in annotations and not refresh_uniprot:
            continue
        uniprot_data = uniprot_annotations.get(uniprot_id, {})
        cofactors = uniprot_data.get('cofactors') or []
        catalytic_substrates = uniprot_data.get('catalytic_substrates') or []
        resolved_cofactors = resolve_ligand_list(cofactors, ligand_cache)
        resolved_substrates = resolve_ligand_list(catalytic_substrates, ligand_cache)
        selected_ligands = unique_ligands(resolved_cofactors + resolved_substrates)
        annotations[locus_tag] = {
            'locus_tag': locus_tag,
            'uniprot_id': uniprot_id,
            'protein_name': uniprot_data.get('protein_name') or UNKNOWN,
            'sequence': sequences.get(locus_tag),
            'sequence_length': len(sequences.get(locus_tag, '')) if sequences.get(locus_tag) else None,
            'cofactors': cofactors if cofactors else UNKNOWN,
            'resolved_cofactors': resolved_cofactors if resolved_cofactors else UNKNOWN,
            'catalytic_substrates': catalytic_substrates if catalytic_substrates else UNKNOWN,
            'resolved_catalytic_substrates': resolved_substrates if resolved_substrates else UNKNOWN,
            'selected_ligands': selected_ligands if selected_ligands else UNKNOWN,
            'subunit_text': uniprot_data.get('subunit_text') or UNKNOWN,
            'quaternary_structure': uniprot_data.get('quaternary_structure') or UNKNOWN,
            'copies': int(uniprot_data.get('copies') or 1),
            'boltz': {
                'run': bool(sequences.get(locus_tag)),
                'needs_manual_check': any(
                    ligand.get('needs_manual_smiles')
                    for ligand in selected_ligands
                    if isinstance(ligand, dict)
                ),
            },
        }
    return annotations


