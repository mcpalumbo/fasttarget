def cofactor_names(cofactors):
    """
    Convert parsed UniProt cofactor records into a compact TSV field.

    :param cofactors: List of cofactor dictionaries or Unknown.
    :return: Pipe-separated cofactor names, or Unknown.
    """
    if not cofactors or cofactors == UNKNOWN:
        return UNKNOWN
    return '|'.join(sorted({cof.get('name', UNKNOWN) for cof in cofactors if cof.get('name')})) or UNKNOWN


def ligand_field(ligands, field):
    """
    Convert ligand dictionaries into a pipe-separated TSV field.

    :param ligands: List of ligand dictionaries or Unknown.
    :param field: Ligand dictionary field to extract.
    :return: Pipe-separated values, or Unknown.
    """
    if not ligands or ligands == UNKNOWN:
        return UNKNOWN
    values = []
    for ligand in ligands:
        if isinstance(ligand, dict) and ligand.get(field):
            values.append(ligand.get(field))
    return pipe_join(values)


