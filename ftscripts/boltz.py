def write_boltz_summary(annotations, curation, model_paths, results_dir, pockets_dir, summary_tsv):
    """
    Write mergeable Boltz summary table.

    :param annotations: Dictionary keyed by locus_tag with Boltz annotations.
    :param curation: Dictionary keyed by locus_tag with curated Boltz parameters.
    :param model_paths: Dictionary keyed by locus_tag with normalized model paths.
    :param results_dir: Boltz results directory.
    :param pockets_dir: Boltz pockets directory.
    :param summary_tsv: Output summary TSV path.
    :return: Summary table as a pandas DataFrame.
    """
    rows = []
    for locus_tag in sorted(curation):
        cur = curation[locus_tag]
        if not cur['run_boltz']:
            continue
        ann = annotations.get(locus_tag, {})
        model_path = model_paths.get(locus_tag)
        confidence = parse_model_confidence(results_dir, locus_tag)
        fpocket_score, fpocket_pocket = parse_best_fpocket(os.path.join(pockets_dir, locus_tag))
        p2rank_score, p2rank_pocket = parse_best_p2rank(os.path.join(pockets_dir, locus_tag))

        rows.append({
            'gene': locus_tag,
            'uniprot': ann.get('uniprot_id'),
            'boltz_structure': os.path.basename(model_path) if model_path else None,
            'boltz_model_path': model_path,
            'boltz_quaternary_structure': cur.get('quaternary_structure'),
            'boltz_copies': cur.get('copies'),
            'boltz_cofactors_uniprot': cur.get('cofactors_uniprot'),
            'boltz_substrates_uniprot': cur.get('substrates_uniprot'),
            'boltz_selected_ligand_names': '|'.join(cur.get('selected_ligand_names', [])),
            'boltz_selected_ligand_smiles': '|'.join(cur.get('selected_ligand_smiles', [])),
            'boltz_confidence_score': confidence.get('confidence_score'),
            'boltz_ptm': confidence.get('ptm'),
            'boltz_iptm': confidence.get('iptm'),
            'boltz_ligand_iptm': confidence.get('ligand_iptm'),
            'boltz_protein_iptm': confidence.get('protein_iptm'),
            'boltz_complex_plddt': confidence.get('complex_plddt'),
            'boltz_complex_iplddt': confidence.get('complex_iplddt'),
            'boltz_druggability_score': fpocket_score,
            'boltz_fpocket_pocket': fpocket_pocket,
            'boltz_p2rank_probability': p2rank_score,
            'boltz_p2rank_pocket': p2rank_pocket,
        })

    df = pd.DataFrame(rows)
    df.to_csv(summary_tsv, sep='\t', index=False)
    print(f'Boltz summary table saved to {summary_tsv}')
    return df


def build_arg_parser():
    """
    Build command-line argument parser.

    :return: Configured argparse.ArgumentParser.
    """
    parser = argparse.ArgumentParser(
        description='Standalone Boltz workflow for FastTarget structure results.'
    )
    parser.add_argument('--output-path', default=None, help='FastTarget output path containing organism folders.')
    parser.add_argument('--organism-name', default=None, help='Organism name used by FastTarget.')
    parser.add_argument('--structures-dir', default=None, help='Direct path to organism/<organism>/structures.')
    parser.add_argument('--gbk-file', default=None, help='Path to the organism GenBank file.')
    parser.add_argument('--cache', default='/grupos/Marce/estructural/boltz/boltz_cache', help='Boltz cache path.')
    parser.add_argument('--boltz-executable', default='boltz', help='Boltz executable name/path.')
    parser.add_argument('--container-engine', default='docker', choices=['docker', 'singularity', 'apptainer'], help='Container engine for FPocket/P2Rank.')
    parser.add_argument('--cpus', type=int, default=1, help='CPUs for P2Rank.')
    parser.add_argument('--stage', default='all', choices=['annotations', 'yaml', 'predict', 'models', 'pockets', 'summary', 'all'], help='Workflow stage to run.')
    parser.add_argument('--refresh-uniprot', action='store_true', help='Refetch UniProt annotations even if JSON exists.')
    parser.add_argument('--overwrite-curation', action='store_true', help='Overwrite existing curation TSV.')
    parser.add_argument('--skip-boltz-run', action='store_true', help='Create inputs and parse existing results without running boltz predict.')
    parser.add_argument('--skip-pockets', action='store_true', help='Skip FPocket/P2Rank execution and only parse existing pocket outputs.')
    parser.add_argument('--no-msa-server', action='store_true', help='Do not pass --use_msa_server to Boltz.')
    return parser


def main(argv=None):
    """
    Run the standalone Boltz workflow.

    :param argv: Optional list of command-line arguments.
    """
    parser = build_arg_parser()
    args = parser.parse_args(argv)

    organism_name, structures_dir = infer_organism_name(args.output_path, args.organism_name, args.structures_dir)
    if not os.path.isdir(structures_dir):
        raise FileNotFoundError(f'Structures directory not found: {structures_dir}')

    dirs = create_boltz_folders(structures_dir)
    annotations_json = os.path.join(dirs['boltz'], 'boltz_annotations.json')
    curation_tsv = os.path.join(dirs['boltz'], 'boltz_curation.tsv')
    summary_tsv = os.path.join(dirs['boltz'], f'{organism_name}_boltz_summary.tsv')

    gbk_file = find_gbk_file(args.output_path, organism_name, args.gbk_file, structures_dir)
    mapping = load_locus_uniprot_mapping(structures_dir, organism_name)
    sequences = get_locus_sequences(gbk_file)

    stages = ['annotations', 'yaml', 'predict', 'models', 'pockets', 'summary'] if args.stage == 'all' else [args.stage]

    annotations = None
    if 'annotations' in stages or not os.path.isfile(annotations_json):
        annotations = build_boltz_annotations(mapping, sequences, args.refresh_uniprot, annotations_json)
        write_annotations_json(annotations, annotations_json)
        write_curation_tsv(annotations, curation_tsv, overwrite=args.overwrite_curation)
    else:
        annotations = files.json_to_dict(annotations_json)

    curation = read_curation_tsv(curation_tsv)

    yaml_files = {}
    if any(stage in stages for stage in ['yaml', 'predict', 'models', 'pockets', 'summary']):
        yaml_files = write_boltz_yaml_files(annotations, curation, dirs['inputs'])

    if 'predict' in stages and not args.skip_boltz_run:
        run_boltz_predict(
            dirs['inputs'],
            dirs['results'],
            args.cache,
            use_msa_server=not args.no_msa_server,
            output_format='pdb',
            boltz_executable=args.boltz_executable,
        )

    model_paths = {}
    if any(stage in stages for stage in ['models', 'pockets', 'summary']):
        model_paths = normalize_boltz_models(yaml_files, dirs['results'], dirs['models'])

    if 'pockets' in stages and not args.skip_pockets:
        run_boltz_pockets(model_paths, dirs['pockets'], container_engine=args.container_engine, cpus=args.cpus)

    if 'summary' in stages:
        if not model_paths:
            model_paths = normalize_boltz_models(yaml_files, dirs['results'], dirs['models'])
        write_boltz_summary(annotations, curation, model_paths, dirs['results'], dirs['pockets'], summary_tsv)


if __name__ == '__main__':
    main()
