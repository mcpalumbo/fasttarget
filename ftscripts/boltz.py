def run_boltz_pockets(model_paths, pockets_dir, container_engine='docker', cpus=1):
    """
    Run FPocket and P2Rank for normalized Boltz PDB models.

    :param model_paths: Dictionary keyed by locus_tag with normalized model paths.
    :param pockets_dir: Directory where pocket outputs will be written.
    :param container_engine: Container engine for FPocket and P2Rank.
    :param cpus: Number of CPUs for P2Rank.
    """
    structures = get_structures_module()
    for locus_tag, model_path in tqdm(model_paths.items(), desc='Boltz pocket prediction'):
        if not model_path.endswith('.pdb'):
            logging.warning(f'Skipping pockets for non-PDB Boltz model: {model_path}')
            continue

        locus_pockets_dir = os.path.join(pockets_dir, locus_tag)
        os.makedirs(locus_pockets_dir, exist_ok=True)
        model_parent = os.path.dirname(model_path)

        try:
            structures.fpocket_for_structure(model_path, model_parent, locus_pockets_dir, container_engine=container_engine)
        except Exception as exc:
            logging.exception(f'FPocket failed for {locus_tag}: {exc}')

        try:
            structures.p2rank_for_structure(
                model_path,
                model_parent,
                locus_pockets_dir,
                cpus,
                alphafold=True,
                container_engine=container_engine
            )
        except Exception as exc:
            logging.exception(f'P2Rank failed for {locus_tag}: {exc}')


def parse_best_fpocket(locus_pockets_dir):
    """
    Parse the best FPocket result from a Boltz locus pocket directory.

    :param locus_pockets_dir: Path to structures/boltz/pockets/<locus_tag>.
    :return: Tuple with druggability score and pocket ID.
    """
    structures = get_structures_module()
    folders = [
        os.path.join(locus_pockets_dir, folder)
        for folder in os.listdir(locus_pockets_dir)
        if folder.endswith('_fpocket') and os.path.isdir(os.path.join(locus_pockets_dir, folder))
    ] if os.path.isdir(locus_pockets_dir) else []
    if not folders:
        return None, None
    pockets_dict = structures.pockets_data_to_dict(folders[0])
    best = structures.pockets_filter(pockets_dict)
    key = next(iter(best.keys()), None)
    value = best.get(key) if key else None
    if value == 'No_pockets' or not isinstance(value, dict):
        return None, 'No_pockets'
    return value.get('maxDS'), value.get('pocket')


